const { PrismaClient } = require('@prisma/client');
const fs = require('fs');
const path = require('path');

const prisma = new PrismaClient();

// Doƒüru CSV parsing fonksiyonu
function parseCSVLine(line) {
    const result = [];
    let current = '';
    let inQuotes = false;

    for (let i = 0; i < line.length; i++) {
        const char = line[i];

        if (char === '"') {
            inQuotes = !inQuotes;
        } else if (char === ',' && !inQuotes) {
            result.push(current);
            current = '';
        } else {
            current += char;
        }
    }

    result.push(current);
    return result;
}

function parseRecipeCSV(content) {
    const lines = content.split('\n').filter(line => line.trim());
    const recipes = [];
    let currentRecipe = null;

    for (let i = 0; i < lines.length; i++) {
        const line = lines[i].trim();
        if (!line) continue;

        // CSV parsing - tƒ±rnak i≈üaretli deƒüerleri doƒüru ≈üekilde ayrƒ±≈ütƒ±r
        const cols = parseCSVLine(line);

        // Re√ßete ba≈ülƒ±ƒüƒ± tespiti
        const isRecipeHeader = cols[0] &&
            (cols[0].includes('(UR)') || cols[0].includes('(YM)')) &&
            !cols[1] && !cols[2];

        if (isRecipeHeader) {
            // √ñnceki re√ßeteyi kaydet
            if (currentRecipe && currentRecipe.ingredients.length > 0) {
                recipes.push(currentRecipe);
            }

            const recipeName = cols[0].replace('(UR)', '').replace('(YM)', '').trim();
            currentRecipe = {
                name: recipeName,
                ingredients: []
            };
        }
        // Stok adƒ± ba≈ülƒ±ƒüƒ± deƒüilse ve malzeme satƒ±rƒ±ysa
        else if (currentRecipe && cols[0] && cols[1] && cols[2] &&
            cols[0] !== 'Stok Adƒ±' &&
            !cols[0].includes('Fire') &&
            cols[0] !== '') {

            const materialName = cols[0].trim();
            const unit = cols[1].trim();
            let amount = cols[2].trim();

            // Tƒ±rnak i≈üaretlerini temizle ve virg√ºl√º noktaya √ßevir
            amount = amount.replace(/"/g, '').replace(',', '.');

            if (amount && amount !== '' && !isNaN(parseFloat(amount))) {
                currentRecipe.ingredients.push({
                    materialName: materialName,
                    amount: parseFloat(amount),
                    unit: unit.toLowerCase()
                });
            }
        }
    }

    // Son re√ßeteyi ekle
    if (currentRecipe && currentRecipe.ingredients.length > 0) {
        recipes.push(currentRecipe);
    }

    return recipes;
}

// Malzeme adƒ± mapping (CSV'den database'e)
function mapMaterialName(csvName) {
    const mapping = {
        'SADE YAG': 'SADEYAƒû',
        'FISTIK': 'ƒ∞√á FISTIK',
        'TOZ SEKER': 'TOZ ≈ûEKER',
        'GLIKOZ': 'GLƒ∞KOZ',
        'YOGURT': 'YOƒûURT',
        'KARAKOYUNLU UN': 'KARAKOYUNLU UN',
        'TEKSIN UN': 'TEKSƒ∞N UN',
        'YUMURTA': 'YUMURTA',
        'SU': 'SU',
        'TUZ': 'TUZ',
        'NISASTA': 'NI≈ûASTA',
        'LIMON': 'Lƒ∞MON',
        'SUT': 'S√úT',
        'CEVIZ': 'CEVƒ∞Z',
        'KADAYIF': 'KADAYIF',
        'MAYDANOZ': 'MAYDANOZ',
        'ANTEP PEYNIRI': 'ANTEP PEYNƒ∞Rƒ∞',
        'SODA GR': 'SODA GR',
        'IRMIK NO:0': 'IRMIK NO:0',
        'IRMIK NO:3': 'IRMIK NO:3'
    };

    return mapping[csvName.toUpperCase()] || csvName;
}

// Birim d√∂n√º≈ü√ºm√º
function convertToKgAndBirim(amount, unit) {
    const unitLower = unit.toLowerCase();

    switch (unitLower) {
        case 'gr':
            return { miktar: amount / 1000, birim: 'KG' };
        case 'kg':
            return { miktar: amount, birim: 'KG' };
        case 'ml':
            return { miktar: amount / 1000, birim: 'LITRE' };
        case 'lt':
            return { miktar: amount, birim: 'LITRE' };
        case 'adet':
            return { miktar: amount * 0.05, birim: 'KG' }; // 1 yumurta = 50g
        default:
            return { miktar: amount, birim: 'KG' };
    }
}

async function main() {
    console.log('üìä CSV VERƒ∞LERƒ∞NDEN RE√áETE Mƒ∞KTARLARI G√úNCELLENƒ∞YOR...\n');

    try {
        // CSV dosyasƒ±nƒ± oku
        const csvPath = path.join(__dirname, '../veriler/Kurallar ve kodlar.xlsx - Re√ßeteler.csv');
        const csvContent = fs.readFileSync(csvPath, 'utf-8');

        // CSV'yi parse et
        const csvRecipes = parseRecipeCSV(csvContent);
        console.log(`üìã ${csvRecipes.length} re√ßete CSV'den okundu`);

        // Mevcut re√ßeteleri ve malzemeleri al
        const [recipes, materials] = await Promise.all([
            prisma.recipe.findMany({
                include: {
                    icerikelek: {
                        include: {
                            material: true
                        }
                    }
                }
            }),
            prisma.material.findMany()
        ]);

        console.log(`üîç ${recipes.length} re√ßete, ${materials.length} malzeme veritabanƒ±nda mevcut\n`);

        let updatedRecipeCount = 0;
        let updatedIngredientCount = 0;
        let notFoundCount = 0;

        for (const csvRecipe of csvRecipes) {
            try {
                // Re√ßeteyi veritabanƒ±nda bul
                const dbRecipe = recipes.find(r =>
                    r.ad.toUpperCase().includes(csvRecipe.name.toUpperCase()) ||
                    csvRecipe.name.toUpperCase().includes(r.ad.toUpperCase())
                );

                if (!dbRecipe) {
                    console.log(`‚ö†Ô∏è  Re√ßete bulunamadƒ±: ${csvRecipe.name}`);
                    notFoundCount++;
                    continue;
                }

                console.log(`üîÑ G√ºncelleniyor: ${dbRecipe.ad}`);
                let recipeUpdated = false;

                // Malzemeleri g√ºncelle
                for (const csvIngredient of csvRecipe.ingredients) {
                    const mappedName = mapMaterialName(csvIngredient.materialName);

                    // Malzemeyi bul
                    const material = materials.find(m =>
                        m.ad.toUpperCase() === mappedName.toUpperCase()
                    );

                    if (!material) {
                        console.log(`   ‚ö†Ô∏è  Malzeme bulunamadƒ±: ${mappedName}`);
                        continue;
                    }

                    // Re√ßetedeki bu malzemeyi bul
                    const dbIngredient = dbRecipe.icerikelek.find(ing =>
                        ing.materialId === material.id
                    );

                    if (!dbIngredient) {
                        console.log(`   ‚ö†Ô∏è  Re√ßetede malzeme bulunamadƒ±: ${material.ad}`);
                        continue;
                    }

                    // Miktar ve birim d√∂n√º≈ü√ºm√º
                    const { miktar, birim } = convertToKgAndBirim(
                        csvIngredient.amount,
                        csvIngredient.unit
                    );

                    // G√ºncelle
                    await prisma.recipeIngredient.update({
                        where: { id: dbIngredient.id },
                        data: {
                            miktar: miktar,
                            birim: birim
                        }
                    });

                    console.log(`   ‚úÖ ${material.ad}: ${miktar} ${birim} (CSV: ${csvIngredient.amount} ${csvIngredient.unit})`);
                    updatedIngredientCount++;
                    recipeUpdated = true;
                }

                if (recipeUpdated) {
                    // Re√ßete maliyetini yeniden hesapla
                    const updatedRecipe = await prisma.recipe.findUnique({
                        where: { id: dbRecipe.id },
                        include: {
                            icerikelek: {
                                include: {
                                    material: true
                                }
                            }
                        }
                    });

                    let toplamMaliyet = 0;
                    for (const ingredient of updatedRecipe.icerikelek) {
                        const materialPrice = ingredient.material.birimFiyat || 0;
                        let ingredientCost = 0;

                        if (ingredient.birim === 'GRAM') {
                            ingredientCost = (ingredient.miktar / 1000) * materialPrice;
                        } else if (ingredient.birim === 'ML') {
                            ingredientCost = (ingredient.miktar / 1000) * materialPrice;
                        } else {
                            ingredientCost = ingredient.miktar * materialPrice;
                        }

                        toplamMaliyet += ingredientCost;
                    }

                    await prisma.recipe.update({
                        where: { id: dbRecipe.id },
                        data: {
                            toplamMaliyet: toplamMaliyet,
                            porsinoyonMaliyet: toplamMaliyet / (updatedRecipe.porsiyon || 1)
                        }
                    });

                    updatedRecipeCount++;
                }

            } catch (error) {
                console.error(`‚ùå ${csvRecipe.name} g√ºncellenirken hata:`, error.message);
            }
        }

        console.log('\nüéâ RE√áETE Mƒ∞KTARLARI CSV VERƒ∞LERƒ∞YLE G√úNCELLENDƒ∞!');
        console.log(`üìä ƒ∞statistikler:`);
        console.log(`   ‚úÖ G√ºncellenen re√ßete: ${updatedRecipeCount}`);
        console.log(`   ‚úÖ G√ºncellenen malzeme: ${updatedIngredientCount}`);
        console.log(`   ‚ö†Ô∏è  Bulunamayan: ${notFoundCount}`);

        // √ñrnek g√ºncellenmi≈ü re√ßete g√∂ster
        console.log('\nüìù √ñrnek g√ºncellenmi≈ü re√ßete:');
        const sampleRecipe = await prisma.recipe.findFirst({
            include: {
                icerikelek: {
                    include: {
                        material: true
                    }
                }
            },
            where: {
                toplamMaliyet: { gt: 0 }
            }
        });

        if (sampleRecipe) {
            console.log(`üç∞ ${sampleRecipe.ad}:`);
            console.log(`   üí∞ Toplam maliyet: ${sampleRecipe.toplamMaliyet?.toFixed(2) || 0}‚Ç∫`);
            console.log(`   ü•Ñ Malzemeler:`);
            sampleRecipe.icerikelek.slice(0, 5).forEach(ing => {
                console.log(`      ‚Ä¢ ${ing.material.ad}: ${ing.miktar} ${ing.birim}`);
            });
        }

    } catch (error) {
        console.error('‚ùå HATA:', error);
        throw error;
    }
}

main()
    .catch((e) => {
        console.error(e);
        process.exit(1);
    })
    .finally(async () => {
        await prisma.$disconnect();
    }); 