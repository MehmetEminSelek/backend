const fs = require('fs');
const path = require('path');
const csv = require('csv-parser');
const { PrismaClient } = require('@prisma/client');

const prisma = new PrismaClient();
const CSV_PATH = path.join(__dirname, '../../veriler/Re√ßeteler.csv');
const MATERIALS_CSV_PATH = path.join(__dirname, '../../veriler/Hammade ve Yarƒ± Mam√ºller Kodlar.csv');

function readCSV(filePath, encoding = 'utf8') {
    return new Promise((resolve, reject) => {
        const results = [];
        if (!fs.existsSync(filePath)) {
            reject(new Error(`CSV bulunamadƒ±: ${filePath}`));
            return;
        }
        fs.createReadStream(filePath, { encoding })
            .pipe(csv())
            .on('data', (row) => results.push(row))
            .on('end', () => resolve(results))
            .on('error', reject);
    });
}

function mapStokToMaterialKod(stokAdiRaw) {
    if (!stokAdiRaw) return null;
    const stokAdi = String(stokAdiRaw).trim().toUpperCase();
    const map = {
        // Hammaddeler (√∂rnekler; CSV‚Äônize g√∂re geni≈ületilebilir)
        'SADE YAG': 'HM012',
        'ANTEP PEYNIRI': 'HM001',
        'MAYDANOZ': 'HM010',
        'FISTIK': 'HM006',
        'TOZ SEKER': 'HM017',
        'SU': 'HM014',
        'GLIKOZ': 'HM003',
        'IRMIK NO:0': 'HM004',
        'IRMIK NO:3': 'HM005',
        'YOGURT': 'HM019',
        'SODA GR': 'HM013',
        'KARAKOYUNLU UN': 'HM008',
        'KADAYIF': 'HM007',
        'CEVIZ': 'HM002',
        'TEKSIN UN': 'HM016',
        'YUMURTA': 'HM020',
        'TUZ': 'HM018',
        'NISASTA': 'HM011',
        'LIMON': 'HM009',
        'SUT': 'HM015',
        // Yarƒ± mamuller
        'HAMUR (YM)': 'YM001',
        'SERBET (YM)': 'YM003',
        'KAYMAK (YM)': 'YM002'
    };
    return map[stokAdi] || null;
}

function standardizeBirim(birimRaw, miktar) {
    const b = String(birimRaw || '').toLowerCase();
    if (b === 'kg') return { birim: 'KG', miktar: miktar };
    if (b === 'gr' || b === 'g') return { birim: 'GRAM', miktar: miktar };
    if (b === 'lt' || b === 'l') return { birim: 'LITRE', miktar: miktar };
    if (b === 'ml') return { birim: 'ML', miktar: miktar };
    if (b === 'adet') return { birim: 'ADET', miktar: miktar };
    if (b === 'paket') return { birim: 'PAKET', miktar: miktar };
    // Default
    return { birim: 'KG', miktar: miktar };
}

function parseFireOrani(val) {
    if (!val) return 0;
    if (typeof val === 'number') return val;
    const s = String(val).trim();
    if (!s) return 0;
    if (s.endsWith('%')) {
        const f = parseFloat(s.replace('%', ''));
        return isNaN(f) ? 0 : f / 100;
    }
    const f = parseFloat(s);
    return isNaN(f) ? 0 : f;
}

function parseReceteler(csvRows) {
    const receteler = [];
    let current = null;
    let items = [];

    const getFirstCol = (row) => Object.values(row)[0];

    for (const row of csvRows) {
        const first = getFirstCol(row);

        // Yeni re√ßete ba≈ülangƒ±cƒ±: √ºr√ºn satƒ±rƒ± (√∂r: "BURMA KADAYIF (UR)")
        if (first && (String(first).includes('(UR)') || String(first).includes('(YM)'))) {
            if (current) {
                current.ingredients = items.slice();
                receteler.push(current);
            }
            const ad = String(first).trim();
            current = {
                ad,
                kod: `RCP_${Date.now()}_${receteler.length + 1}`,
                aktif: true,
                aciklama: `${ad} re√ßetesi`
            };
            items = [];
            continue;
        }

        // Malzeme satƒ±rƒ±
        const stokAdi = row['Stok Adƒ±'];
        if (stokAdi && stokAdi !== 'Stok Adƒ±') {
            const birim = row['Birim'];
            const netMiktar = parseFloat(row['Net Miktar']) || 0;
            const fire1 = parseFireOrani(row['Fire1']);
            const fire2 = parseFireOrani(row['Fire2']);
            const gerMktr = parseFloat(row['Ger Mktr']) || netMiktar;

            if (netMiktar > 0) {
                const materialKod = mapStokToMaterialKod(stokAdi);
                items.push({ stokAdi, materialKod, birim, netMiktar, fire1, fire2, gerMktr });
            }
        }
    }
    if (current) {
        current.ingredients = items.slice();
        receteler.push(current);
    }
    return receteler;
}

function normalizeName(s) {
    if (!s) return '';
    let t = String(s).toUpperCase();
    t = t
        .replace(/ƒ∞/g, 'I').replace(/ƒ±/g, 'I')
        .replace(/≈û/g, 'S').replace(/≈ü/g, 'S')
        .replace(/ƒû/g, 'G').replace(/ƒü/g, 'G')
        .replace(/√ú/g, 'U').replace(/√º/g, 'U')
        .replace(/√ñ/g, 'O').replace(/√∂/g, 'O')
        .replace(/√á/g, 'C').replace(/√ß/g, 'C');
    t = t.replace(/[^A-Z0-9 ]/g, ' ').replace(/\s+/g, ' ').trim();
    return t;
}

async function main() {
    console.log('üßæ Re√ßete CSV seed (V2) ba≈ülƒ±yor...');

    // 1) Temizle
    console.log('üßπ Mevcut t√ºm re√ßeteler siliniyor...');
    await prisma.recipeIngredient.deleteMany({});
    await prisma.recipe.deleteMany({});

    // 2) CSV oku ve parse et
    const rows = await readCSV(CSV_PATH);
    const parsed = parseReceteler(rows);
    console.log(`üìÑ Toplam parse edilen re√ßete: ${parsed.length}`);

    // 2.1) Material indekslerini hazƒ±rla
    const allMaterials = await prisma.material.findMany({
        select: { id: true, kod: true, ad: true, birimFiyat: true }
    });
    const codeToMat = new Map();
    const nameToMat = new Map();
    for (const m of allMaterials) {
        if (m.kod) codeToMat.set(String(m.kod).toUpperCase(), m);
        nameToMat.set(normalizeName(m.ad), m);
    }

    // 2.2) Materials CSV‚Äôden isim‚Üíkod haritasƒ±
    let csvNameToCode = new Map();
    try {
        const mats = await readCSV(MATERIALS_CSV_PATH);
        for (const row of mats) {
            const hm = row['HAMMADDE ADI'] || row['Hammadde Adƒ±'] || row['Hammade Adƒ±'];
            const ym = row['YARI MAMUL ADI '] || row['Yarƒ± Mamul Adƒ±'] || row['Yari Mamul Adi'];
            if (hm && hm.trim()) csvNameToCode.set(normalizeName(hm), null); // kodu bilmesek de ad e≈üle≈ütirmesi i√ßin
            if (ym && ym.trim()) csvNameToCode.set(normalizeName(ym), null);
        }
    } catch (e) {
        console.log('‚ÑπÔ∏è  Materials CSV okunamadƒ± (opsiyonel):', e.message);
    }

    let created = 0; let ingCreated = 0;

    for (const r of parsed) {
        try {
            // Re√ßete olu≈ütur
            const recipe = await prisma.recipe.create({
                data: {
                    ad: r.ad,
                    kod: r.kod,
                    aciklama: r.aciklama || '',
                    aktif: true,
                    porsiyon: 1
                },
                select: { id: true }
            });
            created++;

            // ƒ∞√ßerikleri ekle
            let rowNo = 0;
            for (const ing of r.ingredients) {
                try {
                    let material = null;
                    // 1) Kod ile e≈üle≈ütir
                    if (ing.materialKod) {
                        material = codeToMat.get(String(ing.materialKod).toUpperCase()) || null;
                    }
                    // 2) ƒ∞sim ile e≈üle≈ütir (normalize)
                    if (!material) {
                        const norm = normalizeName(ing.stokAdi);
                        material = nameToMat.get(norm) || null;
                        // 2a) Materials CSV ad e≈üle≈ümesi varsa DB‚Äôde en yakƒ±n adƒ± bul
                        if (!material && csvNameToCode.size) {
                            if (csvNameToCode.has(norm)) {
                                // DB'de normalize adƒ± e≈üle≈üen ilk materyali se√ß
                                const candidate = allMaterials.find(m => normalizeName(m.ad) === norm);
                                if (candidate) material = candidate;
                            }
                        }
                        // 3) Yakƒ±n e≈üle≈üme: contains
                        if (!material) {
                            const candidate = allMaterials.find(m => normalizeName(m.ad).includes(norm));
                            if (candidate) material = candidate;
                        }
                    }
                    if (!material) { console.log(`   ‚ö†Ô∏è Material bulunamadƒ±/e≈üle≈ümedi: ${ing.stokAdi}`); continue; }

                    const std = standardizeBirim(ing.birim, parseFloat(ing.netMiktar) || 0);
                    const costUnitPrice = material.birimFiyat || 0;
                    const costTotal = (parseFloat(ing.gerMktr) || std.miktar) * costUnitPrice;
                    rowNo += 1;

                    await prisma.recipeIngredient.create({
                        data: {
                            recipeId: recipe.id,
                            materialId: material.id,
                            miktar: std.miktar,
                            birim: std.birim,
                            fire1: ing.fire1 || 0,
                            fire2: ing.fire2 || 0,
                            gerMiktar: ing.gerMktr || std.miktar,
                            sonFiyat: costUnitPrice,
                            maliyet: costTotal,
                            siraNo: rowNo
                        }
                    });
                    ingCreated++;
                } catch (ie) {
                    console.warn('   ‚ö†Ô∏è Ingredient eklenemedi:', ie.message);
                }
            }
        } catch (e) {
            console.warn('‚ùå Re√ßete olu≈üturulamadƒ±:', r.ad, e.message);
        }
    }

    console.log(`\n‚úÖ Olu≈üturulan re√ßete: ${created}, eklenen kalem: ${ingCreated}`);

    // 3) Toplam maliyetleri g√ºncelle
    console.log('üí∞ Toplam maliyetler g√ºncelleniyor...');
    const all = await prisma.recipe.findMany({ select: { id: true } });
    for (const rec of all) {
        const ings = await prisma.recipeIngredient.findMany({ where: { recipeId: rec.id }, select: { maliyet: true } });
        const total = ings.reduce((s, i) => s + (i.maliyet || 0), 0);
        await prisma.recipe.update({ where: { id: rec.id }, data: { toplamMaliyet: total, birimMaliyet: total, guncellemeTarihi: new Date() } });
    }

    console.log('üéâ Re√ßete CSV seed (V2) tamamlandƒ±.');
}

main()
    .catch((e) => { console.error('‚ùå Hata:', e); process.exit(1); })
    .finally(async () => { await prisma.$disconnect(); }); 